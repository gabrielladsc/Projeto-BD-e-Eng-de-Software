    private void jConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jConfirmarActionPerformed
        int pos = jTable1.getSelectedRow();
        System.out.println(pos);
        
        String codigo;
        String nome;
        String quantidade;
        
        codigo = jcoidgo.getText();
        nome = jNome1.getText();
        quantidade = jQuantidade.getText();
        
        BrinquedoDao brinquedodao = new BrinquedoDao();
        Brinquedo brinq = new Brinquedo();
        
         if (!codigo.equals("") && !nome.equals("") && !quantidade.equals("") ) {
            brinq.setCodigo(codigo);
            brinq.setNome(nome);
            brinq.setQuantidade(quantidade);
            
            if (brinquedodao.editarBrinquedo(brinq)) {
                JOptionPane.showMessageDialog(null, "Brinquedo editado com sucesso!");
                MySqlConnector.closeConnection();
            }
            else{
                JOptionPane.showMessageDialog(null, "Erro ao editar Encomenda!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Algum dos campos encontra-se vazio!");
        }
        
        

        refleshTabela();

    }//GEN-LAST:event_jConfirmarActionPerformed

