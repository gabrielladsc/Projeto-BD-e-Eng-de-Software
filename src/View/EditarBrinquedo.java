    private void refleshTabela() {
        this.dtm.getDataVector().removeAllElements();
        fun = (brinquedo.read());
        if (fun.size() > 0) {
            for (Brinquedo brinquedo : fun) {
                dtm.insertRow(dtm.getRowCount(), new Object[]{
                    brinquedo.getCodigo(),
                    brinquedo.getNome(),
                    brinquedo.getQuantidade()
                    
                });
            }
        } else {
            dtm.insertRow(dtm.getRowCount(), new Object[]{
                "",
                "",
                "",
                "",
                "",
                "",
                ""
            });
        }
    }

    
