  private void refleshTabela() {
        this.dtm.getDataVector().removeAllElements();
        fun = (encomenda.read());
        if (fun.size() > 0) {
            for (Encomenda encomenda : fun) {
                dtm.insertRow(dtm.getRowCount(), new Object[]{
                    encomenda.getCodigo(),
                    encomenda.getDescricao(),
                    encomenda.getNome(),
                    encomenda.getQuantidade()
                    
                });
            }
        } else {
            dtm.insertRow(dtm.getRowCount(), new Object[]{
                "",
                "",
                "",
                "",
                "",
                "",
                ""
            });
        }
    }

