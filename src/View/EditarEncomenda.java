    private void jconfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jconfirmarActionPerformed
        int pos = jTable1.getSelectedRow();
        System.out.println(pos);
        
        String codigo;
        String descricao;
        String nome;
        String quantidade;
        
        codigo = jCodigo.getText();
        descricao = jDescrição.getText();
        nome = jNome.getText();
        quantidade = jQuantidade.getText();
        
        EncomendaDao encomendadao = new EncomendaDao();
        Encomenda enc = new Encomenda();
        
        if (!codigo.equals("") && !descricao.equals("") && !nome.equals("") && !quantidade.equals("") ) {
            enc.setCodigo(codigo);
            enc.setDescricao(descricao);
            enc.setNome(nome);
            enc.setQuantidade(quantidade);
            
            if (encomendadao.editarEncomenda(enc)) {
                JOptionPane.showMessageDialog(null, "Encomenda editado com sucesso!");
                MySqlConnector.closeConnection();
            }
            else{
                JOptionPane.showMessageDialog(null, "Erro ao editar Encomenda!");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Algum dos campos encontra-se vazio!");
        }
        refleshTabela();
        
        
    }//GEN-LAST:event_jconfirmarActionPerformed

